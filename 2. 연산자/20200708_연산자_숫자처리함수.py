print(abs(-5)) # 5 abs는 절대값, -5의 절대값은 5이기에 5가 프린트됨.
print(pow(4, 2)) # 4^2 = 4*4 = 16 ( 제곱 계산 )
print(max(5, 12)) # 12 ( 최댓값 )
print(min(5, 12)) # 5 ( 최솟값 )
print(round(4.99)) # 5 ( round 은 반올림. )

from math import * 
print (floor(4.99)) # 내림. 4
print (ceil(3.14)) # 올림. 4
print(sqrt(16)) # 제곱근. 4