from random import *

print(random()) #0부터 1사이의 임의의 부동소수(float)

print(randrange(100))  # 0부터 99까지의 임의의 정수

print(randint(1,100))   #1부터 100사이의 랜덤 정수

print(uniform(1.0, 49.9))   #1부터 49.9까지의 부동소수(float)

print(randrange(1, 200, 2)) # 1부터 200 사이의 임의의 짝수

